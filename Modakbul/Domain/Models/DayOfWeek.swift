import Foundation

enum DayOfWeek: String, CaseIterable, Codable {
    case sun = "SUNDAY"
    case mon = "MONDAY"
    case tue = "TUESDAY"
    case wed = "WEDNESDAY"
    case thr = "THURSDAY"
    case fri = "FRIDAY"
    case sat = "SATURDAY"
}
